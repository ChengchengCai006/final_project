-- first table: NYC_311
CREATE TABLE IF NOT EXISTS NYC_311 (
   unique_key INTEGER PRIMARY KEY,
   created_date TIMESTAMP,
   complaint_type TEXT,
   location_type TEXT,
   zipcode INTEGER,
   latitude FLOAT,
   longitude FLOAT,
   location TEXT
);

-- second table: NYC_tree
CREATE TABLE IF NOT EXISTS NYC_tree (
   tree_id INTEGER PRIMARY KEY,
   status TEXT,
   health TEXT,
   zipcode INTEGER,
   nta TEXT,
   nta_name TEXT,
   latitude FLOAT,
   longitude FLOAT
);

-- third table: NYC_zipcode
